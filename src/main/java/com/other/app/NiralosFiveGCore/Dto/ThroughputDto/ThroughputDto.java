package com.other.app.NiralosFiveGCore.Dto.ThroughputDto;

public class ThroughputDto {

	private Integer year;
    private Integer month;
    private Integer day;
    private Integer hour;
    private Integer minute;
    private Integer ulData;
    private Integer dlData;
    
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}
	public Integer getMonth() {
		return month;
	}
	public void setMonth(Integer month) {
		this.month = month;
	}
	public Integer getDay() {
		return day;
	}
	public void setDay(Integer day) {
		this.day = day;
	}
	public Integer getHour() {
		return hour;
	}
	public void setHour(Integer hour) {
		this.hour = hour;
	}
	public Integer getMinute() {
		return minute;
	}
	public void setMinute(Integer minute) {
		this.minute = minute;
	}
	public Integer getUlData() {
		return ulData;
	}
	public void setUlData(Integer ulData) {
		this.ulData = ulData;
	}
	public Integer getDlData() {
		return dlData;
	}
	public void setDlData(Integer dlData) {
		this.dlData = dlData;
	}
    
}
