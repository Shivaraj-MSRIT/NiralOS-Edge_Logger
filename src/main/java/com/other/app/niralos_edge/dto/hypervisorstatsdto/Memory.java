package com.other.app.niralos_edge.dto.hypervisorstatsdto;

public class Memory {
	
	private Long used;

    private Long total;

    private Long free;

	public Long getUsed() {
		return used;
	}

	public void setUsed(Long used) {
		this.used = used;
	}

	public Long getTotal() {
		return total;
	}

	public void setTotal(Long total) {
		this.total = total;
	}

	public Long getFree() {
		return free;
	}

	public void setFree(Long free) {
		this.free = free;
	}
    
    


}
